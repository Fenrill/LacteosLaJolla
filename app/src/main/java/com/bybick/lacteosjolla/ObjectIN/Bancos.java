package com.bybick.lacteosjolla.ObjectIN;

public class Bancos {

    String id_lista;
    String lista_grupo;
    String lista;
    int orden;

    public String getId_lista() {
        return id_lista;
    }

    public void setId_lista(String id_lista) {
        this.id_lista = id_lista;
    }

    public String getLista_grupo() {
        return lista_grupo;
    }

    public void setLista_grupo(String lista_grupo) {
        this.lista_grupo = lista_grupo;
    }

    public String getLista() {
        return lista;
    }

    public void setLista(String lista) {
        this.lista = lista;
    }

    public int getOrden() {
        return orden;
    }

    public void setOrden(int orden) {
        this.orden = orden;
    }
}
